  <h1> Você conhece o bullying e o cyberbullying?</h1>
  <p style="margin-top: -1em;"> Atos que são considerados crimes crescem desparadamente com o avanço dos meios tecnológicos, principalmente no meio das redes sociais.</p>
  <img class="img-fluid" src="img/Oqueeocyberbullying.jpg">
  <figcaption class="figure-caption">Pessoa digitando em um notebook.</figcaption>
  <p>Originária das palavras em inglês, cybernetic (cibernético), bully (“valentão”) e do sufixo “ing” (indicando uma ação contínua), 
     o cyberbullying surgiu com o fortalecimento da web, fazendo com que os usuários tivessem acesso a diversos tipos de informação. 
     Estas podem ser repassadas com a mesma facilidade e intenção de que foram criadas.
      <p>O cyberbullying é um termo referente à
     prática do bullying. <mark> O bullying, segundo a constituição brasileira de 1988, é a violência verbal, física ou psicológica. </mark> Ou seja, 
     se trata de atos de intimidação, humilhação ou discriminação e, ainda: ataques físicos, insultos pessoais, apelidos pejorativos, 
     expressões preconceituosas, etc. O Brasil é o segundo país com maior número de casos de Cyberbullying, ficando atrás apenas da Índia. </p>

     <p> O bullying, visto sobre uma óptica atemporal, resulta no desequilíbrio de poder que há entre o agressor e a vítima, tendo como
     principal objetivo causar problemas físicos e psicológicos na criança, adolescente ou, até mesmo, em um adulto. <mark> Este é separado
     em dois tipos: o tipo verbal e emocional.</mark> O primeiro, se refere à atribuição de apelidos vergonhosos a vítima, com a intenção de
     humilhar, manipular e ridicularizar a vítima. O tipo emocional, por sua vez, é aquele responsável por interferir no relacionamento
     social da vítima, dando-se principalmente em ambiente escolar e - como sendo o nosso foco principal - na internet.
</p>
<h3>Bullying não é brincadeira</h3>
<p> Às vezes, o bullying e o cyberbullying parecem brincadeiras, mas não são. Há um limite sensível e
      muito importante entre brincadeiras e bullying/cyberbullying, especialmente quando falamos de “apelidos".
      Afinal, um mesmo apelido pode ser usado de forma carinhosa ou pejorativa.<mark>Sendo assim, não há uma fórmula
      para identificar esse tipo de violência.</mark>É preciso bom senso, consciência sobre a real intenção, noção de
      limites da liberdade de expressão e pensar antes de agir se a agressão fosse contra você.</p>
      <p>No que se refere a crianças e adolescentes, pais e educadores precisam estar atentos, a fim de observar
      os primeiros sinais de eventual violência sistemática (contínua), e saber identificar possíveis vítimas,
      agressores ou testemunhas, agindo imediata e adequadamente.</p>
      <p>De repente o que era muito legal e divertido perde o sentido. Ir a festas, encontrar amigos, participar
      dos jogos e até ir a escola passa ser um fardo. Uma dor de barriga persistente e um cansaço sem razão aparente
      começam a virar desculpa para tudo. <mark>A depender da angústia, alguns sinais físicos também começam a aparecer.
      Apatia. tristeza, crises de ansiedade, agressividade, pânico e distúrbios alimentares </mark>são alguns deles, assim
      como a queda do rendimento escolar.</p>

<h3> Atenção pais/responsáveis e professores! </h3>
<p>Mais do que ficar atentos aos sinais, cumpre às escolas, clubes e agremiações recreativas a obrigação legal de
      instituir o Programa de Prevenção e Combate à Violência Sistemática, nos termos da <mark>Lei 13.185/2015</mark>, adotando
      meios para conscientizar, evitar e combater o bullying. Para tanto, poderão fazer uso de diversas ferramentas, tais
      como: palestras educativas, filmes, questionários, debates etc.</p>
      <p><mark>Se seu filho/aluno é o AGRESSOR</mark>, não se oculte! Educação e conversa estão entre as principais aliadas para
      o combate ao bullying! Então, busque compreender o que o está motivando agir desta forma, o incentive a refletir
      sobre sua atitude e a importância de colocar-se no lugar do outro. Faça-o conhecer as possibilidades dedesdobramentos
      que sua conduta poderá acarretar, o encoraje a desculpar-se e, dependendo da situação e reincidência da prática,
      não deixe de adotar as medidas disciplinares cabíveis.</p>
      <p><mark>Se seu filho/aluno é a VÍTIMA</mark>, acolha-o para queaponte o que lhe aflige, quais são seus ofensores e suas testemunhas.
      Adote medidas para conter os danos (se for preciso, busque um profissional). Conecte-se com suas emoções, as valorize e
      envolva outras pessoas que possam ajudá-los a conter as atitudes do agressor (a escola, por exemplo, quando o agressor for
      colega da escola). </p>
      <p>Agora, o próximo passo é o <mark>DIÁLOGO</mark>. Se você é menor de idade, converse com seus pais, professores ou algum adulto em
      que possa confiar e peça auxílio. Isso porque, quando estamos no meio do problema, é mais difícil encontrarmos a melhor solução.</p>
     
      <h3> Mundo adulto </h3>
      <p>O bullying não afeta apenas crianças e adolescentes, mas também adultos, inclusive no meio profissional, nos clubes,
      condomínios, entre outros. E, sendo criança, adolescente ou adulto, geralmente, <mark>quando somos a vítima, é mais difícil
      adotar as medidas necessárias para conter as violações aos nossos direitos.</mark> Por isso, se você é menor de idade, como vimos,
      busque um adulto que possa ajudá-lo. Mas se você é adulto e vítima, não se envergonhe! Infelizmente, todos estamos expostos e
      podemos ser vítimas de inúmeros crimes e ilícitos, sem que tenhamos qualquer responsabilidade por isso. </p>

      <p><mark>Proteja-se, não apague as provas </mark>(preserve-as, preferencialmente, por meio de uma Ata Notarial, quando possível) e <mark>busque
      auxílio de um profissional</mark>, como um advogado especialista em direito digital, a fim de que adote todas as medidas necessárias
      para localização da origem das ofensas (caso “desconhecida”),<mark> proceda com as devidas providencias para retirada do conteúdo da
      Internet e ingresse com todas as medidas judiciais cabíveis</mark>, tanto no âmbito civil como no criminal, a depender de cada caso.</p>

